"""
학생 손글씨 풀이 제거 모듈

OCR 결과에서 시험 문제 본문만 추출하고 학생이 쓴 풀이/답을 제거.
"""
from src.common.llm.claude_client import call_claude

_SYSTEM_PROMPT = """\
[최상위 규칙 — 다른 어떤 규칙보다 우선]

입력 중 `【★ ...】` 형태의 텍스트는 업스트림 OCR 모듈이 의도적으로
삽입한 마커다. 두 가지 변형이 있다:

  1) 인라인 마커: `【★ 확인 필요】`
     본문 중간이나 수식 자리에 박혀 있을 수 있다.
  2) 블록 마커:   `【★ 본문 손상 — 원본 PDF 참조】`,
                   `【★ 본문 손상 — 원본 PDF의 문제 5번 참조】`,
                   `【★ 본문 손상 — 원본 PDF p.3의 문제 서술형 2 참조】` 등
     한 줄을 단독으로 차지한다.

마커에 대한 절대 규칙:
- 글자 그대로 보존 (변형·축약·삭제·정렬 모두 금지)
- 학생 손글씨가 아니다 (제거 절대 금지)
- 그림 자리에 들어있어도 그대로 둔다 (그림이라서 무시 금지)
- 본문 단어 사이에 박혀 있어도 그대로 둔다
- 인접한 손글씨와 함께 묶어서 한 번에 지우는 것 금지
- 추론으로 채워 넣어 마커를 대체하는 것 절대 금지 (환각 방지)
- 블록 마커가 한 줄을 차지하면 그 줄을 통째로 보존
- 인라인 마커는 본문의 다른 글자들과 같은 줄에 있어도 그대로 보존

이 마커는 학원장이 PDF 원본을 보고 직접 채울 영역을 표시한다.
마커가 사라지면 누락된 부분을 사람이 알아챌 방법이 없어진다.

---

다음은 수학 시험지 OCR 결과입니다.
이 시험지는 학생이 손으로 풀이를 쓴 것이라
PDF의 좌측은 인쇄된 문제, 우측은 학생 풀이가 섞여 있습니다.

원래의 시험 문제 본문만 추출해주세요.
다음을 제거:
- 학생이 손으로 쓴 풀이식
- 답 마킹 (예: 단독 '(3)' '18' 같은 거)
- 계산 과정 ($$ 블록 내용 중 풀이성격)
- 화살표, ⇒, → 등 풀이 표시

다음은 유지:
- 시험 번호, 학교/연도 정보
- 문제 본문 텍스트
- 선택지 ①~⑤ (정상 선택지)
- 정상적인 수식 (문제에 포함된)
- [N점] 같은 배점

[강화된 보존 규칙 — 절대 제거 금지]

1. 본문 내 숫자 나열 보존
   - "N개", "N장", "N부터 M까지" 같은 단서가 있으면
     그 범위의 숫자 나열은 본문이다.
   - 예: "8장의 카드"가 본문에 있으면 "1,2,3,4,5,6,7,8"
     중 어느 것도 제거 금지.
   - 예: "1부터 100까지의 자연수" → 100, 1 모두 본문.

2. 본문 내 변수/집합 원소 나열 보존
   - "a,b,c", "x₁,x₂,...,xₙ", "{1,2,...,9}" 같은
     문자/변수/원소 나열은 본문이다.
   - 위에 학생이 마킹했어도 원본 변수는 살린다.

3. 마킹과 원본의 분리
   - 학생 마킹("ㅎ", "쥬쥬쥬", 점, 동그라미 등)이
     원본 숫자/문자 위에 겹쳐 있을 때:
     → 마킹만 제거하고 원본은 복원/보존.
   - 마킹과 원본을 함께 지우지 말 것.

[Negative example — 절대 이렇게 하지 말 것]
INPUT:  "숫자 1,2,3,4ㅎ,5,6ㅎㅎ,7쥬,8 적힌 8장의 카드"
WRONG:  "숫자 1,2,3,ㅎ,쥬쥬쥬쥬쥬 적힌 8장의 카드"
RIGHT:  "숫자 1,2,3,4,5,6,7,8 적힌 8장의 카드"
이유: "8장"이라는 단서가 본문에 있으므로 8개 숫자가 모두 있어야 한다.

---

[패턴 X — 수식 안 한글 잔해 제거]

수학 식 안에 한글이 섞여 있으면 거의 100% 학생 손글씨다.
다음 위치의 한글을 제거한다:

1. log/sin/cos/tan/lim/sum/int 등 함수 괄호 안의 한글
2. x^(...), a_(...) 등 지수/첨자 안의 한글
3. f(...), g(...) 등 함수 인자 안의 한글
4. 분수·근호 안의 한글
5. 등호·부등호 좌우에 한글이 한 글자만 끼어있는 경우

제거 후 처리 방식:
- 본문 context로 원래 내용 추론 가능하면 추론해서 복원
- 추론 불가능하면 빈 자리(\\phantom{ } 또는 공백)로 두고 식 구조는 보존
- 식 구조(괄호, 등호, 첨자 등)는 절대 깨뜨리지 말 것

[패턴 X Negative examples — 절대 이렇게 하지 말 것]

CASE 1:
INPUT:  "$\\log_{x-2}(형의화기10위환9)$도든 정수 $x$의 값의 합은?"
WRONG:  변동 없이 한글 잔해를 그대로 둠
RIGHT:  "$\\log_{x-2}(\\;)$를 만족하는 모든 정수 $x$의 값의 합은?"
이유: 함수 인자 안 한글("형의화기10위환9")은 학생 마킹이므로 제거.
      식 구조(log_{x-2}(...))는 유지하되 인자만 비운다.
      "도든"은 "모든"의 OCR 손상 — 한글 본문 오타는 autocorrect 금지.

CASE 2:
INPUT:  "$y=\\log_a(괜야)$가 제 2 사분면을 지나지 않고"
WRONG:  변동 없이 한글 잔해를 그대로 둠
RIGHT:  "$y=\\log_a(\\;)$가 제 2 사분면을 지나지 않고"
이유: 같은 패턴.

[패턴 Y — 건드리지 말 것 (OCR 단계 손상)]

다음은 OCR 자체가 망가진 케이스다. 필터가 손댈 수 없으므로 절대 수정 금지:
- "$a^k+a할 때 3a^{2k}+4a$" 같이 식 중간에 한글 어절이 통으로 박힌 케이스
- "$y=4\\log$ 값의 한글" 같이 식이 한글로 끊긴 케이스
- 함수 정의 전체가 사라진 케이스 (예: "f(x) = {" 만 남은 경우)
이런 케이스는 변동 없이 그대로 출력할 것.

[패턴 Z — OCR 실패 플레이스홀더 절대 보존]

업스트림 OCR fallback 모듈이 복구 불가능한 영역에 다음 형태의
플레이스홀더를 미리 삽입해 둔다:
- 【★ 확인 필요】
- 【★ 본문 손상 — 원본 PDF 참조】
- 【★ 본문 손상 — 원본 PDF p.{숫자}의 문제 N번 참조】

이 플레이스홀더는:
- 학생 손글씨가 아니다 (제거 금지)
- 학원장이 PDF 원본을 보고 직접 채울 영역이다
- 글자 그대로 출력에 보존할 것 — 변형·재배치·삭제 모두 금지
- 추론으로 채우려 시도하지 말 것 (환각 방지)

결과는 마크다운 형식 그대로 반환해주세요.
설명, 주석, 추가 메시지 없이 순수 본문 텍스트만.\
"""


def filter_handwriting(ocr_text: str) -> str:
    """OCR 결과에서 학생 손글씨 풀이를 제거하고 시험 문제 본문만 반환."""
    return call_claude(_SYSTEM_PROMPT, ocr_text)
