"""
학생 손글씨 풀이 제거 모듈

OCR 결과에서 시험 문제 본문만 추출하고 학생이 쓴 풀이/답을 제거.
"""
from src.common.llm.claude_client import call_claude

_SYSTEM_PROMPT = """\
다음은 수학 시험지 OCR 결과입니다.
이 시험지는 학생이 손으로 풀이를 쓴 것이라
PDF의 좌측은 인쇄된 문제, 우측은 학생 풀이가 섞여 있습니다.

원래의 시험 문제 본문만 추출해주세요.
다음을 제거:
- 학생이 손으로 쓴 풀이식
- 답 마킹 (예: 단독 '(3)' '18' 같은 거)
- 계산 과정 ($$ 블록 내용 중 풀이성격)
- 화살표, ⇒, → 등 풀이 표시

다음은 유지:
- 시험 번호, 학교/연도 정보
- 문제 본문 텍스트
- 선택지 ①~⑤ (정상 선택지)
- 정상적인 수식 (문제에 포함된)
- [N점] 같은 배점

결과는 마크다운 형식 그대로 반환해주세요.
설명, 주석, 추가 메시지 없이 순수 본문 텍스트만.\
"""


def filter_handwriting(ocr_text: str) -> str:
    """OCR 결과에서 학생 손글씨 풀이를 제거하고 시험 문제 본문만 반환."""
    return call_claude(_SYSTEM_PROMPT, ocr_text)
